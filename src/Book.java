
public class Book {
    String author;
    String title;
    String inOut;

    Book(String name, String author, String inOut) {
        this.author = name;
        this. title = author;
        this.inOut = inOut;
    }

    public String getName() {
        return author;
    }

    public String getAuthor() {
        return title;
    }

    public String getInOut() {return inOut;}


}
